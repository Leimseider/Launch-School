let weather = 'volcano eruption'
switch (weather) {
  case 'tornado':
    console.log('Abandon the house.');
    break;
  case 'hail':
    console.log('Board the windows.');
    break;
  case 'volcano eruption':
    console.log('Get the metal umbrellas.')
    break;
  default:
    console.log("Go for a bike ride.");
}